$(document).ready(function() {
    $.ajax({
        url: url(window.location.href) + "/controller/get-sales-data-controller.php",
        method: "POST",
        data: {
            type: "sales-monthly-chart",
        },
        success: function(d) {
            var data = JSON.parse(d);
            var date;
            var last_date = 0;
            var month_total = 0;
            data.forEach(function(entry) {
                date = entry.transaction_datetime.split("-");
                var item_tax = parseFloat(entry.item_tax);
                var item_price = parseFloat(entry.item_price);
                var item_count = parseFloat(entry.item_count);
                var item_total = (((item_tax / 100) * item_price) + item_price);
                if (date[0] != last_date) {
                    month_total = 0;
                    month_total += item_total * item_count;
                    $chart_data[last_date - 1] = month_total;
                    last_date = date[0];
                } else {
                    month_total += item_total * item_count;
                    $chart_data[last_date - 1] = month_total;
                }
            });
            init_chart();
        }
    });
});